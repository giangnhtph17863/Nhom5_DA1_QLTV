    private void loadTable() {
        List<SachModel> ds = _iSachService.getSach();
        DefaultTableModel dtm = (DefaultTableModel) this.tblSach.getModel();
        dtm.setRowCount(0);
        for (SachModel d : ds) {
            dtm.addRow(new Object[]{d.getMaSach(), d.getNXB(), d.getGiaTien(), d.getNamXB(),
                d.getNoiDat(), d.getSoLuong(), d.getTacGia(), d.getTenSach(), d.getMaTL().getTenTL()});
        }
    }

    private void loadCombobox() {

        List<TheLoai> theLoai = _iTheLoaiService.getAllTheLoai();
        DefaultComboBoxModel dfcbo = new DefaultComboBoxModel();
        cboTheLoai.setModel(dfcbo);
        cboTheLoai.addItem("Thể Loại");
        for (TheLoai l : theLoai) {
            dfcbo.addElement(l.getTenTL());
        }
    }

    public boolean checkNgayTra(JTextField txt, JTextField txt2) {
        txt.setBackground(white);
        Date date = XDate.toDate(txt.getText());
        Date date2 = XDate.toDate(txt2.getText());
        if (date.after(date2)) {
            txt2.setBackground(pink);
            XMgsbox.alert(this, "Ngày Trả không được nhỏ hơn Ngày Mượn");
            return false;
        } else {
            return true;
        }
    }

    private PhieuMuonModel getPhieuMuonFromInput() {
        PhieuMuonModel model = new PhieuMuonModel();
        model.setMAPM(1);
        model.setMaKH(XAuther.UserKH);
        model.setTienCoc(Double.parseDouble("0.0"));
        model.setTrangThai("Chưa Duyệt");
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date NgayMuon = dateFormat.parse(txtNgayMuon.getText());
            model.setNgayMuon(NgayMuon);
            Date NgayTra = dateFormat.parse(txtNgayTra.getText());
            model.setNgayTra(NgayTra);
        } catch (ParseException ex) {
            Logger.getLogger(JFrmMuonSach.class.getName()).log(Level.SEVERE, null, ex);
        }
        return model;
    }

