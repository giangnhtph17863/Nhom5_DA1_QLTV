    private void tbnMuonSachActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbnMuonSachActionPerformed
        // TODO add your handling code here:
        this.index = tblSach.getSelectedRow();//lấy vị trí dòng được chọn     
        Sach sa = new Sach();
        sa.setMaSach(tblSach.getValueAt(index, 0) + "");
        sa.setTenSach(tblSach.getValueAt(index, 7) + "");
        sa.setTacGia(tblSach.getValueAt(index, 6) + "");
        sa.setNXB(tblSach.getValueAt(index, 1) + "");
        sa.setGiaTien(Double.parseDouble(tblSach.getValueAt(index, 2) + ""));
        sa.setNoiDat(tblSach.getValueAt(index, 4) + "");

        String maSach = tblSach.getValueAt(index, 0) + "";
        if (ktDSMuon(sa.getMaSach())) {
            XMgsbox.alert(this, "Sách này đã có trong danh mục sách mượn");
        } else {
            XMgsbox.alert(this, "Mượn Sách thành công");
            addDSMuon(sa);
            loadCombobox();
        }

    }//GEN-LAST:event_tbnMuonSachActionPerformed

