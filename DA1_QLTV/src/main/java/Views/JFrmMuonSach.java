    private void loadTableTK(List<SachModel> lstSach) {
        mol = (DefaultTableModel) tblSach.getModel();
        if (lstSach.isEmpty()) {
            return;
        }
        mol.setRowCount(0);//Xóa hết dữ liệu cũ trong table
//        int stt = 1;
        for (SachModel d : lstSach) {
            mol.addRow(new Object[]{d.getMaSach(), d.getNXB(), d.getGiaTien(), d.getNamXB(),
                d.getNoiDat(), d.getSoLuong(), d.getTacGia(), d.getTenSach(), d.getMaTL().getTenTL()});
//            stt++;
        }
    }

    public boolean ktDSMuon(String masach) {
        TableRowSorter<DefaultTableModel> tbl = new TableRowSorter<DefaultTableModel>(mol2);
        tbl.setRowFilter(RowFilter.regexFilter(masach));
        if (tbl.getViewRowCount() != 0) {
            return true;
        }
        return false;
    }

    public void setTongMuon() {
        tongMuon = tblBangMuonSach.getRowCount();
        lblTong.setText(String.valueOf(tongMuon));
        if (tongMuon > gioiHan) {
            lblTong.setForeground(Color.red);
        } else {
            lblTong.setForeground(Color.black);
        }
    }

    public void addDSMuon(Sach sa) {
        mol2.addRow(new Object[]{
            sa.getMaSach(),
            sa.getTenSach(),
            sa.getTacGia(),
            sa.getNXB(),
            sa.getGiaTien(),
            sa.getNoiDat()
        });
        setTongMuon();

    }

    void fillTableSachMuon() {
        DefaultTableModel model = (DefaultTableModel) tblBangMuonSach.getModel();
        model.setRowCount(0); // xoa tat ca cac hang
    }

